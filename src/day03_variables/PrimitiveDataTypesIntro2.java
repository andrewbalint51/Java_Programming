        char ch1 = 'A';
        char ch2 = 65;

        System.out.println(ch1);
        System.out.println(ch2);
        System.out.println("----------------------------------");

        char ch3 = 40000;

        System.out.println(ch3);

        System.out.println("----------------------------------");

        int sum = 'A'+'B';

        System.out.println(sum); //131


        System.out.println("----------------------------------");

       // boolean r1 = 1;

        boolean r4 = true;
